def _toy_test():
    """Quick sanity check: forward/backward pass, correct shapes."""
    print("PlacementGNN toy test")
    print("=" * 50)

    V, E = 20, 40
    G = 16
    device = 'cpu'

    # Random data
    sizes = torch.rand(V, 2, device=device) * 0.3 + 0.05
    edge_index = torch.randint(0, V, (2, E), device=device)
    edge_attr = torch.randn(E, 4, device=device)

    # Build features (now 14D with Laplacian PE + centrality)
    features = build_node_features(sizes, edge_index, V)
    assert features.shape == (V, 14), f"features shape {features.shape}"
    print(f"  Node features: {features.shape}")

    # Model (node_input_dim=14 by default now)
    model = PlacementGNN(grid_size=G)
    n_params = sum(p.numel() for p in model.parameters())
    print(f"  Parameters: {n_params:,}")

    # Forward
    logits = model(features, edge_index, edge_attr)
    assert logits.shape == (V, G * G), f"logits shape {logits.shape}"
    print(f"  Logits shape: {logits.shape}")

    # Backward
    logits.sum().backward()
    print(f"  Backward: OK")

    # Decode
    from differentiable_hpwl import build_grid_centers
    grid_centers = build_grid_centers(G)

    pos_hard = decode_heatmap_hard(logits.detach(), grid_centers)
    assert pos_hard.shape == (V, 2), f"hard decode shape {pos_hard.shape}"
    assert pos_hard.min() >= -1.0 and pos_hard.max() <= 1.0
    print(f"  Hard decode: {pos_hard.shape}, range [{pos_hard.min():.3f}, {pos_hard.max():.3f}]")

    pos_soft = decode_heatmap_soft(logits, grid_centers, tau=1.0)
    assert pos_soft.shape == (V, 2)
    print(f"  Soft decode: {pos_soft.shape}, range [{pos_soft.min():.3f}, {pos_soft.max():.3f}]")

    pos_argmax = decode_heatmap_argmax(logits.detach(), grid_centers)
    assert pos_argmax.shape == (V, 2)
    print(f"  Argmax decode: {pos_argmax.shape}")

    # Gradient flow through soft decode
    model.zero_grad()
    logits2 = model(features, edge_index, edge_attr)
    pos_soft2 = decode_heatmap_soft(logits2, grid_centers, tau=0.5)
    loss = pos_soft2.sum()
    loss.backward()
    has_grad = any(p.grad is not None and p.grad.abs().sum() > 0
                   for p in model.parameters())
    assert has_grad, "Gradient must flow through soft decode"
    print(f"  Gradient through soft decode: OK")

    print("\nAll tests passed!")


if __name__ == '__main__':
    _toy_test()
